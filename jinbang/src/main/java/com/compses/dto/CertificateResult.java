package com.compses.dto;

public class CertificateResult {
	private String deviceID;
	private String devOutID;
	private String belongStaID;
	private String result;

	public String getDeviceID() {
		return deviceID;
	}

	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}

	public String getDevOutID() {
		return devOutID;
	}

	public void setDevOutID(String devOutID) {
		this.devOutID = devOutID;
	}

	public String getBelongStaID() {
		return belongStaID;
	}

	public void setBelongStaID(String belongStaID) {
		this.belongStaID = belongStaID;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

}
